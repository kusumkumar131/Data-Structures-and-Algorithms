start=int(input("Enter your number:" ))
old_age=start+10
print("Your age after 10 years will be:", old_age)

# to convert int to float
new_age=float(old_age)
print("Your age after 10 years in float will be:", new_age)

# to convert int to string
new_age_str=str(new_age)
print("Your age after 10 years in string will be:", new_age_str)
print(type(new_age_str))

# to convert int to boolean
new_age_bool=bool(new_age)
print("Your age after 10 years in boolean will be:", new_age_bool)
 #to convert float to int
new_age_int=int(new_age)

print("Your age after 10 years in int will be:", new_age_int)
